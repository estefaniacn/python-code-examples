# Example

name = "Gino"

if name == "Gino":
    print("Hi, Gino!")

# If/else statements

name = "Gino"

if name == "Gino":
    print("Hi, Gino!")
else:
    print("Hey! Your name is NOT Gino.")

# If/elif/else statements

x = 6

if x > 3:
    print("Hello, World!")
elif x > 0:
    print("Python is awesome")
else:
    print("Have an awesome day")
